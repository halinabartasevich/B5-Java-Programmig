package day02_print_statement;

public class Shapes {
    public static void main (String [] args) {
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");

        System.out.println();

        System.out.println("   *");
        System.out.println("  ***");
        System.out.println(" *****");

        System.out.println();

        System.out.println("----");
        System.out.println("|  |");
        System.out.println("----")
        ;

    }
}
